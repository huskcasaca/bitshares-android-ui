package bitshareskit.objects

import java.util.*

enum class ObjectType(val id: Int) {
    NULL_OBJECT(0),
    BASE_OBJECT(1),
    ACCOUNT_OBJECT(2),
    ASSET_OBJECT(3),
    FORCE_SETTLEMENT_OBJECT(4),
    COMMITTEE_MEMBER_OBJECT(5),
    WITNESS_OBJECT(6),
    LIMIT_ORDER_OBJECT(7),
    CALL_ORDER_OBJECT(8),
    CUSTOM_OBJECT(9),
    PROPOSAL_OBJECT(10),
    OPERATION_HISTORY_OBJECT(11),
    WITHDRAW_PERMISSION_OBJECT(12),
    VESTING_BALANCE_OBJECT(13),
    WORKER_OBJECT(14),
    BALANCE_OBJECT(15),
    HTLC_OBJECT(16),
    CUSTOM_AUTHORITY_OBJECT(17),
    TICKET_OBJECT(18),
    LIQUIDITY_POOL_OBJECT(19),

    GLOBAL_PROPERTY_OBJECT(0),
    DYNAMIC_GLOBAL_PROPERTY_OBJECT(1),
//    RESERVED_OBJECT_TYPE(2),
    ASSET_DYNAMIC_DATA(3),
    ASSET_BITASSET_DATA(4),
    ACCOUNT_BALANCE_OBJECT(5),
    ACCOUNT_STATISTICS_OBJECT(6),
    TRANSACTION_OBJECT(7),
    BLOCK_SUMMARY_OBJECT(8),
    ACCOUNT_TRANSACTION_HISTORY_OBJECT(9),
    BLINDED_BALANCE_OBJECT(10),
    CHAIN_PROPERTY_OBJECT(11),
    WITNESS_SCHEDULE_OBJECT(12),
    BUDGET_RECORD_OBJECT(13),
    SPECIAL_AUTHORITY_OBJECT(14),
    BUYBACK_OBJECT(15),
    FBA_ACCUMULATOR_OBJECT(16),
    COLLATERAL_BID_OBJECT(17),

    ORDER_HISTORY_OBJECT(0),
    BUCKET_OBJECT(1);

    val namespace: Namespace
        get() = when (this) {
            in NULL_OBJECT..LIQUIDITY_POOL_OBJECT -> Namespace.PROTOCOL_SPACE
            in GLOBAL_PROPERTY_OBJECT..COLLATERAL_BID_OBJECT -> Namespace.IMPLEMENTATION_SPACE
            in ORDER_HISTORY_OBJECT..BUCKET_OBJECT -> Namespace.ACCOUNT_HISTORY_SPACE
            else -> Namespace.NULL_SPACE
        }

    override fun toString(): String {
        return name.toLowerCase(Locale.ROOT)
    }

}

































